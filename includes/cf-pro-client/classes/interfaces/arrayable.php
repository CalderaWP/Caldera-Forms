<?php


namespace calderawp\calderaforms\pro\interfaces;


/**
 * Class arrayable
 *
 * Use to define common to array method
 *
 * @package calderawp\calderaforms\pro
 */
interface arrayable
{

	/**
	 * Convert object to array
	 *
	 * @since 0.0.1
	 *
	 * @return array
	 */
	public function toArray();
}
